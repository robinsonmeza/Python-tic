import calculadora
num1 = 10
num2 = 5

resultado_suma = calculadora.suma(num1, num2)
print (f"suma: {num1} + {num2} = {resultado_suma}")

resultado_resta = calculadora.resta(num1, num2)
print (f"resta: {num1} - {num2} = {resultado_resta}")

resultado_multi = calculadora.multi(num1, num2)
print (f"multi: {num1} * {num2} = {resultado_multi}")

resultado_divi = calculadora.divi(num1, num2)
print (f"division: {num1} / {num2} = {resultado_divi}")

